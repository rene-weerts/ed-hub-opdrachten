const fruit = "Appels";

switch (fruit) {
    case 'Bananen':
        console.log('Het zijn bananen!');
        break;
    case 'Appels':
        console.log('Het zijn appels!');
        break;
    case 'Citroenen':
        console.log('Het zijn citroenen!');
        break;
    default:
        console.log('Er is geen vrucht gekozen');
}